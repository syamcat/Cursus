#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	t_list	*ptr;
	int		count;

	count = 0;
	ptr = lst;
	while (ptr)
	{
		count++;
		ptr = ptr -> next;
	}
	return (count);
}
