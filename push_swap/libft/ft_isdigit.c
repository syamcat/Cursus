#include <stdio.h>

int	ft_isdigit(char *c)
{
	int	i;

	i = 0;
	printf("str: %s\n", c);
	while (c[i])
	{
		if (c[i] < '0' || c[i] > '9')
		{
			if (!(i == 0 && c[i] == '-'))
				return (0);
		}
		i++;
	}
	return (1);
}
