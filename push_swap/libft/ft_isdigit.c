int	ft_isdigit(char *c)
{
	int	i;

	i = 0;
	while (c[i])
	{
		if ((i == 0 && c[i] != '-') && (c[i] < '0' || c[i] > '9'))
			return (0);
		i++;
	}
	return (1);
}
